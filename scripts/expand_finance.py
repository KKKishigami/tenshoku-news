import re, os

def get_article_body(fn):
    with open(f'articles/{fn}', 'r', encoding='utf-8') as f:
        return f.read()

def replace_article_body(fn, new_body_html):
    html = get_article_body(fn)
    pattern = r'(<article class="article-body">).*?(</article>)'
    new_html = re.sub(pattern, r'\1\n' + new_body_html + r'\n        \2', html, flags=re.DOTALL)
    with open(f'articles/{fn}', 'w', encoding='utf-8') as f:
        f.write(new_html)

TABLE_STYLE = 'style="width:100%;border-collapse:collapse;margin:1rem 0;font-size:0.9rem;"'
TH_STYLE = 'style="background:#1a3a5c;color:#fff;"'

def th(s): return f'<th style="padding:0.75rem;text-align:left;">{s}</th>'
def thc(s): return f'<th style="padding:0.75rem;text-align:center;">{s}</th>'
def td(s): return f'<td style="padding:0.75rem;">{s}</td>'
def tdc(s): return f'<td style="padding:0.75rem;text-align:center;">{s}</td>'
def tr_odd(*cells): return '<tr style="border-bottom:1px solid #e2e8f0;">' + ''.join(cells) + '</tr>'
def tr_even(*cells): return '<tr style="background:#f8fafc;border-bottom:1px solid #e2e8f0;">' + ''.join(cells) + '</tr>'

def highlight(points):
    lis = ''.join(f'<li>{p}</li>\n' for p in points)
    return f"""          <div class="highlight-box">
            <strong>📌 この記事のポイント</strong>
            <ul style="margin-top:0.5rem;padding-left:1.2rem;">
{lis}            </ul>
          </div>"""

